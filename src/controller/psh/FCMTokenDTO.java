package controller.psh;


public class FCMTokenDTO {
	
	private String token_index;
	private String token;
	private String id;
	
	
	public String getToken_index() {
		return token_index;
	}
	public void setToken_index(String token_index) {
		this.token_index = token_index;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
}
